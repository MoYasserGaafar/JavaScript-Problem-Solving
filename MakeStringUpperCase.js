//  Make String Upper Case

//  DESCRIPTION:
//  ==> Write a function which converts the input string to upper case.

function upperCase(str) {
    return str.toUpperCase();
    //str.toUpperCase() : Built-in JavaScript method that converts all the characters in a string to uppercase.
}

console.log(upperCase("Asterisk")); //Output: ASTERISK
// Step 1 :   str is assigned the value "Asterisk"
// Step 2 :   str is assigned the value "Asterisk"
// Step 3 :   Function returns "ASTERISK"